	<h1>How do you verify a collection ?</h1>

	<p>We divided the NFT Collection verification process in 4 phases:</p>
	<ol class="list-decimal list-inside">
		<li>
			<b>Submission</b> - The collection is submitted with all its details (contract address, name...)
			and secured by a deposit.
		</li>
		<li><b>Voting</b> - Communities vote for projects to verify</li>
		<li>
			<b>Challenge</b> - Challengers check all the submissions that passed through the voting phase to
			find scams, suspicious or incorrects ones, incentivized by a bounty.
		</li>
		<li>
			<b>Result</b> - The result is published publicly in a decentralized way. If there were no challenge,
			the collection is verified. Otherwise it goes to Kleros Court for arbitration.
		</li>
	</ol>

